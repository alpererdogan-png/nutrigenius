**If deficient (test first):**
- **Iron** — target ferritin 50–70 ng/mL (4 hours from levothyroxine)

**Avoid:**
- Iodine supplements, kelp, seaweed extract
- Any "thyroid support" blend containing iodine

**Consider:**

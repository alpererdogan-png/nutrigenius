## Menopause Supplements: What the Evidence Actually Supports

Menopause affects every system in the body — cardiovascular, skeletal, neurological, metabolic. While hormone replacement therapy (HRT) remains the most effective treatment for vasomotor symptoms and is first-line for many women, not everyone can or wants to use it. For those seeking complementary or alternative approaches, several supplements have meaningful clinical evidence.

**Important disclaimer:** These supplements complement medical care. If your symptoms are severe, discuss HRT with your doctor — the risk-benefit profile has shifted significantly since the 2002 WHI scare, and modern formulations are much safer than previously feared.

## Hot Flashes and Night Sweats

### Black Cohosh

**Dose:** 20–40 mg standardised extract daily
**Evidence strength:** Moderate-to-strong

Black cohosh (*Actaea racemosa*) is the most studied herbal remedy for menopausal vasomotor symptoms. A 2012 Cochrane review analysing 16 RCTs found that black cohosh significantly reduced hot flash frequency and severity compared to placebo. A 2017 meta-analysis in *Maturitas* confirmed a meaningful reduction in both frequency and intensity.

The mechanism is not fully understood — it does not appear to act as a phytoestrogen but may influence serotonin receptors and thermoregulatory pathways. Most trials use standardised extracts (such as Remifemin). Effects typically emerge within 4–8 weeks.

**Safety note:** Rare cases of liver injury have been reported. Use standardised, reputable products, and avoid if you have liver disease.

### Vitamin E

**Dose:** 400 IU/day
**Evidence strength:** Moderate

A 2007 RCT in *Gynecologic and Obstetric Investigation* (n=120) found 400 IU vitamin E daily reduced hot flash severity significantly compared to placebo. It is not as potent as black cohosh or HRT, but it is well-tolerated and may benefit women with mild vasomotor symptoms. Vitamin E also supports cardiovascular health, which becomes increasingly relevant post-menopause.


## Menopause Supplements: What the Evidence Actually Supports

Menopause affects every system in the body — cardiovascular, skeletal, neurological, metabolic. While hormone replacement therapy (HRT) remains the most effective treatment for vasomotor symptoms and is first-line for many women, not everyone can or wants to use it. For those seeking complementary or alternative approaches, several supplements have meaningful clinical evidence.

**Important disclaimer:** These supplements complement medical care. If your symptoms are severe, discuss HRT with your doctor — the risk-benefit profile has shifted significantly since the 2002 WHI scare, and modern formulations are much safer than previously feared.

## Hot Flashes and Night Sweats

### Black Cohosh

**Dose:** 20–40 mg standardised extract daily
**Evidence strength:** Moderate-to-strong

Black cohosh (*Actaea racemosa*) is the most studied herbal remedy for menopausal vasomotor symptoms. A 2012 Cochrane review analysing 16 RCTs found that black cohosh significantly reduced hot flash frequency and severity compared to placebo. A 2017 meta-analysis in *Maturitas* confirmed a meaningful reduction in both frequency and intensity.

The mechanism is not fully understood — it does not appear to act as a phytoestrogen but may influence serotonin receptors and thermoregulatory pathways. Most trials use standardised extracts (such as Remifemin). Effects typically emerge within 4–8 weeks.

**Safety note:** Rare cases of liver injury have been reported. Use standardised, reputable products, and avoid if you have liver disease.

### Vitamin E

**Dose:** 400 IU/day
**Evidence strength:** Moderate

A 2007 RCT in *Gynecologic and Obstetric Investigation* (n=120) found 400 IU vitamin E daily reduced hot flash severity significantly compared to placebo. It is not as potent as black cohosh or HRT, but it is well-tolerated and may benefit women with mild vasomotor symptoms. Vitamin E also supports cardiovascular health, which becomes increasingly relevant post-menopause.

### Isoflavones and Phytoestrogens

**Dose:** 40–80 mg isoflavones/day (from soy or red clover)
**Evidence strength:** Mixed

Soy isoflavones (genistein, daidzein) have weak oestrogenic activity and have been studied extensively. A 2015 meta-analysis in *Menopause* found a modest but statistically significant reduction in hot flash frequency. However, not all women respond equally — those who produce equol (a gut metabolite of daidzein) may benefit more.

**Concerns:** The phytoestrogen debate continues. Current evidence suggests dietary-level soy intake is safe, even in breast cancer survivors. However, concentrated isoflavone supplements at high doses remain controversial for women with oestrogen-receptor-positive cancers. Discuss with your oncologist if relevant.

## Bone Protection: The Critical Trio

Osteoporosis risk **doubles** after menopause due to oestrogen withdrawal. Bone-protective supplementation should begin at perimenopause, not after a fracture.

### Calcium

**Dose:** 1,000–1,200 mg/day (from diet + supplements combined)
**Evidence strength:** Strong

The evidence for calcium in bone health is well-established. However, there is an important nuance: **excessive calcium supplementation** (>1,500 mg/day without K2) may increase cardiovascular calcification risk. The goal is to fill dietary gaps, not mega-dose. Most women need 500–600 mg supplemental calcium if their diet includes some dairy or fortified foods.

### Vitamin D3

**Dose:** 2,000–4,000 IU/day (adjust based on blood levels)
**Evidence strength:** Strong

Vitamin D is essential for calcium absorption. Post-menopausal women are at high risk for deficiency due to reduced skin synthesis with age and often limited sun exposure. Target blood levels of 40–60 ng/mL (100–150 nmol/L). Use D3 (cholecalciferol), not D2 — it raises blood levels 87% more effectively.

### Vitamin K2 (MK-7)

**Dose:** 100–200 mcg/day
**Evidence strength:** Moderate

K2 directs calcium into bones and away from arteries. A 2013 RCT in *Osteoporosis International* found MK-7 supplementation (180 mcg/day for 3 years) significantly reduced age-related bone loss in post-menopausal women. It is the critical third element of the calcium-D3-K2 triad.

## Sleep and Mood

### Magnesium

**Dose:** 300–400 mg glycinate at bedtime
**Evidence strength:** Moderate

Magnesium glycinate supports both sleep quality and mood. Menopause frequently disrupts sleep architecture, and magnesium deficiency is common in this population. The glycine carrier itself has calming properties and has been shown to lower core body temperature — a key trigger for sleep onset. A 2012 RCT in the *Journal of Research in Medical Sciences* confirmed improved sleep quality in elderly subjects with supplemental magnesium.

## Energy and Libido

### Maca Root

**Dose:** 2,000–3,000 mg/day
**Evidence strength:** Moderate

Maca (*Lepidium meyenii*) is a Peruvian root vegetable that has been used traditionally for energy and fertility. A 2008 RCT in *Menopause* (n=124) found 3.5 g/day maca significantly reduced psychological symptoms including anxiety and depression, and improved sexual dysfunction scores compared to placebo. A 2015 systematic review in *BMC Complementary Medicine and Therapies* confirmed benefits for sexual dysfunction and psychological symptoms.

Maca does not contain phytoestrogens and does not appear to affect hormone levels directly, making it suitable for women who need to avoid oestrogenic compounds.

## When to Talk to Your Doctor

Supplements are most useful for mild-to-moderate symptoms. Seek medical advice if:

- Hot flashes severely disrupt sleep or daily function
- You have risk factors for osteoporosis (family history, low BMI, smoking, early menopause)
- You are considering HRT — modern bioidentical formulations have a much more favourable risk profile than older synthetic options
- You take medications that may interact with supplements (especially blood thinners with vitamin E or K2)

## Bone Density Monitoring

Request a DEXA scan at menopause onset as a baseline, then every 2 years. Supplements slow bone loss, but you need data to track whether your strategy is working. Do not assume you are protected without measurement.

## A Practical Menopause Stack

For most peri- and post-menopausal women:

- **Calcium** 500–600 mg (supplement to fill dietary gap)
- **Vitamin D3** 2,000–4,000 IU daily
- **Vitamin K2 (MK-7)** 100–200 mcg daily
- **Magnesium glycinate** 300–400 mg at bedtime
- **Black cohosh** 20–40 mg standardised extract (for vasomotor symptoms)
- **Maca** 2–3 g daily (for energy, libido, mood)

Allow 8–12 weeks for full effects. Start one supplement at a time so you can identify what helps.

